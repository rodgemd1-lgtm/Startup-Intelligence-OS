#!/usr/bin/env bash
# V10.0 Layer 1 — Auto-HANDOFF on Session End
# Writes a minimal HANDOFF.md if significant work was done.
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/../.." && pwd)"

# Check if there are uncommitted changes (proxy for "work was done")
cd "$ROOT_DIR"
CHANGES=$(git diff --stat 2>/dev/null | tail -1)
UNTRACKED=$(git ls-files --others --exclude-standard 2>/dev/null | head -5)

if [[ -z "$CHANGES" && -z "$UNTRACKED" ]]; then
    # No changes detected, skip handoff
    exit 0
fi

# Get current branch
BRANCH=$(git branch --show-current 2>/dev/null || echo "unknown")

# Get active workspace context
ACTIVE_COMPANY=""
ACTIVE_PROJECT=""
WORKSPACE_FILE="$ROOT_DIR/.startup-os/workspace.yaml"
if [[ -f "$WORKSPACE_FILE" ]]; then
    ACTIVE_COMPANY=$(grep 'active_company:' "$WORKSPACE_FILE" 2>/dev/null | head -1 | sed 's/.*: *//' || echo "unknown")
    ACTIVE_PROJECT=$(grep 'active_project:' "$WORKSPACE_FILE" 2>/dev/null | head -1 | sed 's/.*: *//' || echo "unknown")
fi

# Get recent commit messages
RECENT_COMMITS=$(git log --oneline -5 2>/dev/null || echo "no commits")

# Write HANDOFF.md
cat > "$ROOT_DIR/HANDOFF.md" << HANDOFF_EOF
# Session Handoff
Date: $(date '+%Y-%m-%d %H:%M')
Branch: $BRANCH
Active Company: $ACTIVE_COMPANY
Active Project: $ACTIVE_PROJECT

## Changes Detected
$CHANGES

## Recent Commits
$RECENT_COMMITS

## Untracked Files
$(echo "$UNTRACKED" | head -10)

## Next Steps
- Review uncommitted changes
- Continue from where this session left off

_Auto-generated by V10.0 session-end hook_
HANDOFF_EOF

exit 0
