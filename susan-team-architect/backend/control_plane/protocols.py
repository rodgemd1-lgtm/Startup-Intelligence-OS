def _keyword_in_text(text: str, keyword: str) -> bool:
    normalized = keyword.strip().lower()
    if not normalized:
        return False

    prefix = r"\b" if normalized[0].isalnum() else ""
    suffix = r"\b" if normalized[-1].isalnum() else ""
    pattern = f"{prefix}{re.escape(normalized)}{suffix}"
    return re.search(pattern, text) is not None


def _keyword_hits(text: str, keywords: list[str]) -> list[str]:
    return [keyword for keyword in keywords if _keyword_in_text(text, keyword)]


def _contains_any(text: str, keywords: list[str]) -> bool:
    return any(_keyword_in_text(text, keyword) for keyword in keywords)


def _route_candidates(task: str, company_id: str, agent_registry: dict[str, Any]) -> list[dict[str, Any]]:
    task_lower = task.lower()
    agents = agent_registry.get("agents", {})
    scores: dict[str, int] = {}
    reasons: dict[str, list[str]] = {}

    def add(agent_id: str, score: int, reason: str) -> None:
        if agent_id not in agents:
            return
        scores[agent_id] = scores.get(agent_id, 0) + score
        reasons.setdefault(agent_id, []).append(reason)

    broad_keywords = ["plan", "build", "launch", "roadmap", "company", "go-to-market", "go to market", "strategy", "architect"]
    if _contains_any(task_lower, broad_keywords) or len(task.split()) >= 10:
        add("susan", 4, "Broad cross-functional request benefits from Susan orchestration.")
    else:
        add("susan", 1, "Susan should remain in the loop as the company architect.")

    for agent_id in _company_defaults(company_id):
        add(agent_id, 2, f"Core operator for {company_id}.")

    if company_id == "transformfit" and _contains_any(
        task_lower,
        [
            "ux",
            "ui",
            "design",
            "screen",
            "workout",
            "session",
            "logging",
            "onboarding",
            "coach",
            "coaching",
            "flow",
        ],
    ):
        for agent_id, boost in TRANSFORMFIT_EXPERIENCE_BENCH.items():
            add(agent_id, boost, "TransformFit experience request should pull the fuller design/session/coaching bench.")

    if company_id == "transformfit" and _contains_any(
        task_lower,
        [
            "love map",
            "love maps",
            "therapeutic alliance",
            "perceived responsiveness",
            "personal knowledge map",
            "relational endowment",
        ],
    ):
        add("freya", 5, "Relational coaching requests need behavioral science depth.")
        add("flow", 5, "Relational coaching requests need sports psychology depth.")

    for agent_id, keywords in ROUTE_KEYWORDS.items():
        hits = _keyword_hits(task_lower, keywords)
        if hits:
            add(agent_id, 3 + len(hits), f"Matched task language: {', '.join(hits[:3])}.")

    for agent_id, payload in agents.items():
        role_tokens = {
            token.strip(" ,/()-").lower()
            for token in f"{payload.get('role', '')} {payload.get('group', '')}".split()
            if len(token.strip(" ,/()-")) > 3
        }
        matched = sorted(token for token in role_tokens if token and _keyword_in_text(task_lower, token))
        if matched:
            add(agent_id, 1, f"Role overlap: {', '.join(matched[:3])}.")

    ranked = sorted(
        (
            {
                "agent_id": agent_id,
                "score": score,
                "reasons": reasons.get(agent_id, []),
                "group": agents[agent_id].get("group", ""),
                "rag_data_types": agents[agent_id].get("rag_data_types", []),
            }
            for agent_id, score in scores.items()
            if score > 0
        ),
        key=lambda item: (-item["score"], item["agent_id"]),
    )
    return ranked


def _route_data_types(task: str, ranked_agents: list[dict[str, Any]], evidence_hits: list[dict[str, Any]]) -> list[str]:
    task_lower = task.lower()
    recommended: list[str] = []
    seen: set[str] = set()

    for data_type, keywords in DATA_TYPE_KEYWORDS.items():
        if _contains_any(task_lower, keywords):
            recommended.append(data_type)
            seen.add(data_type)

    for candidate in ranked_agents:
        for data_type in candidate.get("rag_data_types", []):
            if data_type and data_type not in seen:
                recommended.append(data_type)
                seen.add(data_type)

    for hit in evidence_hits:
        data_type = hit.get("data_type")
        if data_type and data_type not in seen:
            recommended.append(data_type)
            seen.add(data_type)

    return recommended[:8]


def search_company_knowledge(
    query: str,
    company_id: str = "shared",
    data_types: list[str] | None = None,
    top_k: int = 8,
) -> list[dict[str, Any]]:
    retriever = Retriever()
    supabase = create_client(config.supabase_url, config.supabase_key)
    results = retriever.search(query, company_id=company_id, data_types=data_types, top_k=top_k)
    if company_id != "shared":
        shared_results = retriever.search(query, company_id="shared", data_types=data_types, top_k=top_k)
        seen_ids = {hit.get("id") for hit in results}
        for hit in shared_results:
            if hit.get("id") not in seen_ids:
                results.append(hit)
        results = sorted(results, key=lambda item: item.get("similarity", 0.0), reverse=True)[:top_k]
    row_lookup: dict[str, dict[str, Any]] = {}
    ids = [hit.get("id") for hit in results if hit.get("id")]
    if ids:
        rows = (
            supabase.table("knowledge_chunks")
            .select("id,source,source_url,metadata")
            .in_("id", ids)
            .execute()
        )
        row_lookup = {row["id"]: row for row in (rows.data or [])}
    formatted: list[dict[str, Any]] = []
    for hit in results:
        row = row_lookup.get(hit.get("id"), {})
        metadata = (row.get("metadata") or hit.get("metadata") or {})
        formatted.append(
            {
                "similarity": round(hit.get("similarity", 0.0), 4),
                "data_type": hit.get("data_type"),
                "title": metadata.get("title") or metadata.get("name") or row.get("source") or row.get("source_url") or "Untitled",
                "source": row.get("source"),
                "source_url": row.get("source_url"),
                "public_url": metadata.get("public_url"),
                "excerpt": (hit.get("content") or "")[:650],
                "metadata": metadata,
            }
        )
    return formatted


def get_company_status(company_id: str) -> dict[str, Any]:
    retriever = Retriever()
    supabase = create_client(config.supabase_url, config.supabase_key)
    companies = _companies()
    output_dir = config.companies_dir / company_id / "susan-outputs"
    output_files: list[dict[str, Any]] = []
    latest_output_at: datetime | None = None

    if output_dir.exists():
        for path in sorted(output_dir.iterdir()):
            if not path.is_file():
                continue
            modified_at = datetime.fromtimestamp(path.stat().st_mtime, UTC)
            latest_output_at = max(latest_output_at, modified_at) if latest_output_at else modified_at
            output_files.append(
                {
                    "name": path.name,
                    "path": str(path),
                    "modified_at": modified_at.isoformat(),
                    "size_bytes": path.stat().st_size,
                }
            )

    visual = (
        supabase.table("knowledge_chunks")
        .select("id", count="exact")
        .eq("company_id", company_id)
        .eq("data_type", "visual_asset")
        .execute()
    )

    return {
        "company_id": company_id,
        "company": companies.get(company_id, {}),
        "chunk_count": retriever.count_chunks(company_id),
        "visual_asset_count": visual.count or 0,
        "output_dir": str(output_dir),
        "output_files": output_files,
        "latest_output_at": latest_output_at.isoformat() if latest_output_at else None,
    }


