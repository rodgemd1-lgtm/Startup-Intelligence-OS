def _render_command_for_target(name: str, company_id: str) -> str:
    susan_root = config.base_dir.parent
    text = (susan_root / "commands" / name).read_text(encoding="utf-8")
    injected = f"SUSAN_DEFAULT_COMPANY={company_id} ./.venv/bin/python scripts/susan_cli.py"
    text = text.replace("./.venv/bin/python scripts/susan_cli.py", injected)
    hints = {
        'argument-hint: "\\"query\\" --company company_id [--types a,b] [--top-k N]"':
            'argument-hint: "\\"query\\" [--company company_id] [--types a,b] [--top-k N]"',
        'argument-hint: "company_id [--mode quick|deep|design|foundry|full] [--refresh]"':
            'argument-hint: "[company_id] [--mode quick|deep|design|foundry|full] [--refresh]"',
        'argument-hint: "company_id"':
            'argument-hint: "[company_id]"',
        'argument-hint: "company_id [--limit N]"':
            'argument-hint: "[company_id] [--limit N]"',
        'argument-hint: "company_id \\"task\\" [--top-k N]"':
            'argument-hint: "[company_id] \\"task\\" [--top-k N]"',
    }
    for current, replacement in hints.items():
        text = text.replace(current, replacement)
    return text


def sync_project_target(target: dict[str, Any]) -> dict[str, Any]:
    root = Path(target["path"])
    claude_commands = root / ".claude" / "commands"
    susan_dir = root / ".susan"
    susan_agents = susan_dir / "agents"
    susan_hooks = susan_dir / "hooks"

    claude_commands.mkdir(parents=True, exist_ok=True)
    susan_agents.mkdir(parents=True, exist_ok=True)
    susan_hooks.mkdir(parents=True, exist_ok=True)

    susan_root = config.base_dir.parent
    for name in SOURCE_COMMANDS:
        (claude_commands / name).write_text(_render_command_for_target(name, target["company_id"]), encoding="utf-8")

    for agent_file in (susan_root / "agents").glob("*.md"):
        shutil.copy2(agent_file, susan_agents / agent_file.name)

    shutil.copy2(susan_root / "hooks" / "hooks.json", susan_hooks / "hooks.json")
    shutil.copy2(susan_root / "SUSAN_PROTOCOLS.md", susan_dir / "PROTOCOLS.md")

    agents = [agent for agent in _company_defaults(target["company_id"]) if agent in _registry().get("agents", {})]
    _write_project_readme(root, target["company_id"], target["project_label"], agents)
    _write_project_context(root, target["company_id"], target["project_label"], agents)
    _write_protocol_skill(root, target["company_id"], target["project_label"], agents)
    _install_project_mcp(root)
    _install_project_settings(root)

    return {
        "status": "synced",
        "path": str(root),
        "company_id": target["company_id"],
        "commands": len(SOURCE_COMMANDS),
        "agents": len(list((susan_root / "agents").glob("*.md"))),
        "mcp_server": PROJECT_MCP_SERVER_ID,
        "skill": "susan-protocols",
    }


def sync_project_protocols(config_path: Path | None = None) -> list[dict[str, Any]]:
    targets_path = config_path or (config.data_dir / "project_protocol_targets.yaml")
    payload = yaml.safe_load(targets_path.read_text(encoding="utf-8")) or {}
    results: list[dict[str, Any]] = []
    for target in payload.get("targets", []):
        root = Path(target["path"])
        if not root.exists():
            results.append({"status": "missing", "path": str(root), "company_id": target.get("company_id")})
            continue
        results.append(sync_project_target(target))
    return results
